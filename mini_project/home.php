  ความหมายของบั้งไฟ<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; คำว่า<strong>&nbsp;“</strong><strong>บั้งไฟ</strong><strong>”</strong>&nbsp;ในภาษาถิ่นอีสานมักจะสับสนกับคำว่า “<strong>บ้องไฟ</strong>” แต่ที่ถูกนั้นควรเรียกว่า”บั้งไฟ”ดังที่ เจริญชัย ดงไพโรจน์ ได้อธิบายความแตกต่างของคำทั้งสองไว้ว่า บั้งหมายถึง สิ่งที่เป็นกระบอก เช่น บั้งทิง สำหรับใส่น้ำดื่ม หรือบั้งข้าวหลาม เป็นต้น

  <div style="text-align: center;"><img alt="UploadImage" src="http://www.govesite.com/uploads/20161018093812naSsPrh/data/20170214145535.jpg"></div>
  <strong>การเอาหมื่อใส่กระบอกบั้งไฟ</strong><br>
  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; ส่วนคำว่า&nbsp;<strong><u>บ้อง</u></strong>&nbsp;หมายถึง สิ่งของใดๆ ก็ได้ที่มี 2 ชิ้น มาสวมหรือประกอบเข้ากันได้ ส่วนนอกเรียกว่า บ้อง ส่วนในหรือสิ่งที่เอาไปสอดใสจะเป็นสิ่งใดก็ได้ เช่น บ้องมีด บ้องขวาน บ้องเสียม บ้องวัว บ้องควาย ดังนั้น คำว่า บั้งไฟ ในภาษาถิ่นอีสานจึงเรียกว่า บั้งไฟ ซึ่งหมายถึงดอกไม้ไฟชนิดหนึ่ง มีหางยาวเอาดินประสิวมาคั่วกับถ่านไม้ตำให้เข้ากันจนละเอียดเรียกว่า หมื่อ (ดินปืน) และเอาหมื่อนั้นใส่กระบอกไม้ไผ่ตำให้แน่นเจาะรูตอนท้ายของบั้งไฟ เอาไผ่ท่อนอื่นมัดติดกับกระบอกให้ใส่หมื่อโดยรอบ เอาไม้ไผ่ยาวลำหนึ่งมามัดประกบต่อออกไปเป็นหางยาว สำหรับใช้ถ่วงหัวให้สมดุลกัน เรียกว่า “<strong>บั้งไฟ</strong>” ในทัศนะของผู้วิจัย บั้งไฟ คือการนำเอากระบอกไม้ไผ่ เลาเหล็ก ท่อเอสลอน หรือเลาไม้อย่างใดอย่างหนึ่งมาบรรจุหมื่อ (ดินปืน) ตามอัตราส่วนที่ช่างกำหนดไว้แล้วประกอบท่อนหัวและท่อนหางเป็นรูปต่างๆ ตามที่ต้องการ เพื่อนำไปจุดพุ่งขึ้นสู่อากาศ จะมีควันและเสียงดัง&nbsp;บั้งไฟมีหลายประเภท ตามจุดมุ่งหมายของประโยชน์ในการใช้สอย<br>
  ในทางศาสนาพุทธกับประเพณีบุญบั้งไฟ<br>
