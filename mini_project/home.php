  <strong>4. </strong><strong>บั้งไฟแสน</strong><br>
  <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; บั้งไฟชนิดนี้เป็นบั้งไฟขนาดใหญ่ที่สุด บรรจุดินปืนหนัก 120 กิโลกรัมขึ้นไป บั้งไฟขนาดนี้ทำยากที่สุดจะต้องอาศัยความชำนาญเป็นพิเศษ เพราะบั้งไฟขนาดนี้หากแตกแล้วจะเป็นอันตรายมาก เพราะฉะนั้นก่อนทำบั้งไฟจะต้องมีพิธีกรรมบวงสรวงให้ถูกต้องตามหลักการทำบั้งไฟแสนเสียก่อนจึงจะลงมือทำ เมื่อตกบั้งไฟเสร็จเรียบร้อยแล้วจะมีการตกแต่งประดับประดาบั้งไฟ<br>
  &nbsp;<br>
  <strong>5. </strong><strong>บั้งไฟตะไล</strong><br>
  <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; บั้งไฟชนิดนี้ก็คือบั้งไฟจินายขนาดใหญ่นั่นเอง มีความยาวประมาณ 9-12 นิ้ว รูปร่างกลมมีไม้บางๆ แบนๆ เป็นวงกลมครอบหัวท้ายบั้งไฟเมื่อพุ่งขึ้นสู่ฟ้าไปโดยทางขวาง<br>
  &nbsp;<br>
  <strong>6. </strong><strong>บั้งไฟตื้อ</strong><br>
  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; บั้งไฟตื้อหรือบั้งไฟกระแตนั่งตอ เป็นบั้งไฟขนาดเล็กมีหางสั้น วิธีทำ ตัดกระบอกไม้ไผ่ขนาด 1 นิ้วครึ่งยาวประมาณ 3 นิ้ว อัดหมื่อให้แน่นประมาณ 2 นิ้ว ใช้หมื่อถ่านสามหรือถ่านสี่อัดด้วยเถียดไม้ให้แน่น ต่อหางซึ่งทำจากไม้ไผ่ เหลาเป็นแท่งเล็กๆ ใช้เลื่อยตัดมุมข้อออกจนเห็นหมื่อ เจาะให้เป็นรูเล็กๆ แล้วติดชนวน เวลาจะจุดเอาหางเสียบลงในแท่นที่ตั้งพอให้ตั้งได้ จุดชนวนจากด้านบน บั้งไฟจะพุ่งและหมุนขึ้นสู่อากาศ เกิดเสียงดังตือๆ เวลาหมุนจะไม่ค่อยมีทิศทาง ใช้จุดในงานศพ เวลาจุดมีอันตรายมากไม่ค่อยนิยมทำกัน<br>
  &nbsp;<br>
  <strong>7. </strong><strong>บั้งไฟพลุ</strong><br>
  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; บั้งไฟพลุ เป็นบั้งไฟที่นิยมจุดในเทศกาลต่างๆ เช่น งานกฐิน&nbsp;งานบุญมหาชาติ หรือ งานเปิดกีฬา ฯลฯ&nbsp;เป็นบั้งไฟที่จุดแล้วทำให้เกิดเสียงดัง ในอดีตนิยมจุดในงานกฐิน เพื่อเป็นการบอกข่าวไปยังพี่น้องประชาชนทั่วไปให้ทราบ<br>
  &nbsp;<br>
  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>บุญบั้งไฟ</strong>&nbsp;นิยมทำกันในเดือนหก ถือเป็นประเพณีสำคัญที่จะขาดไม่ได้ เพราะตั้งแต่โบราณจนถึงปัจจุบัน ชาวอีสานมีความเชื่อว่า ถ้าปีใดไม่จัดงานบุญบั้งไฟ ฟ้าฝนก็จะไม่ตกต้องตามฤดูกาล เกิดความแห้งแล้ง ไม่มีน้ำทำนา แต่ถ้าปีใดจัดงานประเพณีบุญบั้งไฟ ฟ้าฝนก็จะตกต้องตามฤดูกาล เกิดความอุดมสมบูรณ์ ปราศจากโรคภัย&nbsp;งานบุญบั้งไฟจึงถือเป็นงานประเพณี ประจำปีที่สำคัญของชาวอีสาน พอใกล้ถึงวันงานชาวอีสานไม่ว่าจะอยู่ที่ไหนก็จะกลับบ้านไปร่วมงานบุญบั้งไฟซึ่งเป็นงานที่สร้างความรักความสามัคคีของคนท้องถิ่นเป็นอย่างดี<br>
