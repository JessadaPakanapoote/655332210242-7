  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong><u>ลายบั้งไฟ</u></strong>&nbsp;: ใช้ลายศิลปไทย คือ ลายกนก อันเป็นลายพื้นฐานในการลับลายบั้งไฟ โดยช่างจะนิยมใช้กระดาษดังโกทองด้านเป็นพื้นและสีเม็ดมะขามเป็นตัวสับลาย เพื่อให้ลายเด่นชัดในการตกแต่งเพื่อให้ความสวยงาม<br>
  <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong><u>ตัวบั้งไฟ</u></strong>&nbsp;: มีลูกโอ้จะใช้ลายประจำยาม ลายหน้าเทพพนม ลายหน้ากาล ลูกเอ้ใช้ลายประจำยาม ก้ามปูเปลว และลายหน้ากระดาน ฯลฯ<br>
  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong><u>กรวยเชิง</u></strong>&nbsp;: เป็นลวดลายไทยที่เขียนอยู่เชิงยาบที่ประดับพริ้วลงมาจากช่วงตัวบั้งไฟ<br>
  &nbsp;<br>
