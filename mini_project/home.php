  <strong>บั้งไฟขนาดเล็ก</strong><br>
  ส่วนประกอบของบั้งไฟ<br>
  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>1. </strong><strong>เลาบั้งไฟ</strong>&nbsp;เลาบั้งไฟคือส่วนประกิบที่ทำหน้าที่บรรจุดินปืน มีลักษณะเป็นรูปทรงกระบอกกลมยาว มีความยาวประมาณ 1.5 - 7 เมตร ทำด้วยลำไม้ไผ่เล้วใช้ร้วไม้ไผ่ (ตอก) ปิดเป็นเกลียวเชือกพันรอบเลาบั้งไฟอีกครั้งหนึ่งให้แน่น และใช้ดินปืนที่ชาวบ้านเรียกว่า"หมือ" อัดให้แน่นลงไปในเลาบั้งไฟ ด้วยวิธีใช้สากตำแล้วเจาะรูสายชนวน เสร็จแล้วนำเลาบั้งไฟ ไปมัดเข้ากับส่วนหางบั้งไฟ ในสมัดต่อมานิยมนำวัสดุอื่นมาใช้เป็นเลาบั้งไฟแทนไม้ไผ่ ได้แก่ ท่อเหล็ก ท่อพลาสติก เป็นต้น เรียกว่าเลาเหล็กซึ่งสามารถอัดดินปืนได้แน่นและมีประสิทธิภาพในการยิงได้สูงกว่า<br>
  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>&nbsp;2. </strong><strong>หางบั้งไฟ</strong>&nbsp;หางบั้งไฟถือเป็นส่วนสำคัญทำหน้าที่คล้ายหางเสือ ของเรือคือสร้างความสมดุลย์ให้กับบั้งไฟคอยบังคับทิศทางบั้งไฟให้ยิงขึ้นไปในทิศทางตรงและสูง บั้งไฟแบบเดิมนั้น ทำจากไม้ไผ่ทั้งลำ ต่อมาพัฒนาเป็นหางท่อนเหล็กและหางท่อนไม้ไผ่ติดกันหางท่อนเหล็กมีลักษณะเป็นท่อนกลม ทรงกระบอกมีความยาวประมาณ 8-12 เมตร ทำหน้าท่เป็นคานงัดยกลำตัวบั้งไฟชูโด่งชี้เอียงไปข้างหน้าทำมุมประมาณ 30-40 องศากับพื้นดิน โดยบั้งไฟจะยื่นไปข้างหน้ายาวประมาณ 7-8 เมตร ปลายหางด้านหนึ่งตั้งอยู่บนฐานที่ตั้งบั้งไฟ<br>
  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>&nbsp; 3. </strong><strong>ลูกบั้งไฟ</strong>&nbsp;เป็นลำไม้ไผ่ที่นำมาประกอบเลาบั้งไฟโดยมัดรอบลำบั้งไฟ บั้งไฟลำหนึ่งจะประกอบด้วยลูกบั้งไฟประมาณ 8-15 ลูก ขึ้นอยู่กับขนาดของบั้งไฟ เดิมลูกบั้งไฟมีแปดลูกมีชื่อเรียกเรียงตามลำดับคู่ขนาดใหญ่ไปหาคู่ที่มีขนาดเล็กกว่าได้แก่ ลูกโอ้ ลูกกลาง ลูกนางและลูกก้อย ลูกบั้งไฟช่วยให้รูปทรงของบั้งไฟกลมเรียวสวยงาม นอกจากนี้ลูกบั้งไฟยังเป็นพื้นผิวรองรับการเอ้หรือการตกแต่งลวดลายปะติดกระดาษ<br>
