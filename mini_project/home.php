  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong><u>กระรอกเผือก</u></strong>&nbsp;: ท้าวพังคี แปลงร่างมาเพื่อให้นางไอ่หลงใหล<br>
  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong><u>ปล้องคาด</u></strong>&nbsp;: ลายรักร้อย ลายลูกพัดใบเทศ ลายลูกพัดขอสร้อย เป็นต้น<br>
  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong><u>เกริน</u></strong>&nbsp;: เป็นส่วนที่ยื่นออกสองข้างของบุษบก เป็นรูปรอนเบ็ดลายกนก สำหรับตั้งฉัตรท้ายเกริน ราชรถประดับส่วนท้ายของหางบั้งไฟ<br>
  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong><u>บุษบก</u></strong>&nbsp;: เป็นองค์ประกอบไว้บนราชรถ เพื่อสมมุติให้เป็นปราสาทผาแดงนางไอ่<br>
  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong><u>ต้างบั้งไฟ</u></strong>&nbsp;: ลายกระจังปฏิญาณ ลายก้านขด ลายพุ่มข้าวบิณฑ์<br>
  &nbsp;<br>
