  <strong>1. </strong><strong>บั้งไฟโหวด</strong><br>
  <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; บั้งไฟโบดหรือโหวดเป็นบั้งไฟขนาดเล็กตัวกระบอกจะยาวขึ้น ประมาณ 4-10 นิ้ว บรรจุหมื่อหนักประมาณ 1 ส่วน 8 ถึง 1 ส่วน 2 กิโลกรัม ใช้หางยาวประมาณ 1-4 เมตร มีกระบอกไม้ไผ่เล็กๆ มัดวางรอบตัวบั้งไฟ นิยมทำประกอบกันในบั้งไฟใหญ่ (บั้งไฟหมื่น, บั้งไฟแสน) ปัจจุบันไม่ค่อยนิยมทำ เพราะไม่มีช่าง<br>
  &nbsp;<br>
  <strong>2. </strong><strong>บั้งไฟม้า</strong><br>
  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;บั้งไฟชนิดนี้เป็นบั้งไฟขนาดเล็กจุดไปตามทิศทางที่กำหนดใช้เส้นลวดเป็นวิถีตรึงไปยังเป้าหมายที่ต้องการ ลักษณะทั่วไปเป็นบั้งไฟที่ทำจากกระบอกไม้ไผ่ 1 ปล้อง ขนาดแล้วแต่ต้องการ โดยทั่วไปเส้นผ่าศูนย์กลางประมาณ 2 นิ้ว ยาวประมาณ 1 ฟุตทางภาคกลางและภาคอีสานเรียกว่า “ลูกหนู”&nbsp;คล้ายม้าที่กำลังวิ่ง ถ้าติดรูปอะไรก็เรียกชื่อไปตามนั้น เป็นคนขี่ม้า รูปวัว แล้วแต่จะทำรูปอะไร บางครั้งภาคเหนือเรียกว่า บอกไฟยิง<br>
  &nbsp;<br>
  <strong>3. </strong><strong>บั้งไฟช้าง</strong><br>
  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; บั้งไฟชนิดนี้ไม่มีหาง มีชื่ออีกอย่างหนึ่งว่ากระโพกหรือตะโพก เวลาจุดไม่ต้องการให้พุ่งขึ้นไปแต่ต้องการมีเสียงร้องคล้ายกับช้างร้อง วิธีทำบั้งไฟให้ใช้กระบอกไม้ไผ่ที่มีขนาดใหญ่ที่สุดยาวเพียงป้องเดียวให้มีข้อปิดทั้ง 2 ด้าน ทุบไม้ไผ่ให้แตกเล็กน้อย เจาะรู เพื่อบรรจุหมื่อแล้วต่อชนวนเข้ารูแท่งหมื่อทำจากหมื่อถ่าน 3-4 อัดลงในไม้ไผ่ขนาดเล็กให้แน่น แล้วผ่าเอาแท่งหมื่อออกมาคล้ายข้าวหลาม ให้ได้แท่งประมาณ 3 นิ้ว การจุดนั้นนิยมต่อพ่วงชนวนบั้งไฟใหญ่ เวลาจุดชนวนผ่าจะเกิดเสียงดังเหมือนเสียงช้างร้อง นิยมวางต่อกันเป็นช่วงๆ กระบอก ถ้าต้องการจะให้มีเสียงดังอย่างไรก็จะมีเทคนิคในการทำให้เกิดเสียงนั้นๆ<br>
